<?php
  // --------------------
  // -- Types Of Loops --
  // --------------------

  // 1 - For loop
  for ($i = 1; $i <= 10; $i++) {
    echo $i . "\n";
  }

  echo "\n";

  // 2 - While loop
  $x = 1;
  while ($x <= 10) {
    echo $x . "\n";
    $x++;
  }

  echo "\n";

  // 3 - Do while loop
  $j = 1;
  do {
    echo $j . "\n";
    $j++;
  } while ($j <= 10);
